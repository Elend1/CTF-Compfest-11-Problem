

package main

import (
	"fmt"
	"os"
	"bufio"
	"io/ioutil"
)

func main()	{
	file, err := os.Open("flag.txt")
	if err != nil{
		fmt.Println("Oops, something went terribly wrong!")
		os.Exit(0)
	}

	byte_flag, err := ioutil.ReadAll(file)
	if err != nil{
		fmt.Println("Oops, something went terribly wrong!")
		os.Exit(0)
	}

	flag := string(byte_flag[:])
	for i := 0; i < 11; i++	{
		flag += flag
	}
	file.Close()

	heartbleed_prevention := "\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01\x01"

	for true{
		scanner := bufio.NewScanner(os.Stdin)
		scanner.Scan()
		recieved := scanner.Text() + heartbleed_prevention + flag
		runes := []rune(recieved)
		var length_to_send_back uint16
		for _, length := range(runes)	{
			length_to_send_back = uint16(length)
			break
		}
		fmt.Printf("%s\n", string(runes[1:1+length_to_send_back]))
	}
}